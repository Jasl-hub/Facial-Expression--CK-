class TrainingTimeCallback(Callback):
    def on_train_begin(self, logs={}):
        self.start_time = time.time()

    def on_train_end(self, logs={}):
        end_time = time.time()
        training_time = end_time - self.start_time
        print("Training time:", training_time, "seconds")

